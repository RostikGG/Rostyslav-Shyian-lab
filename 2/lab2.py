# Порахувати суму чисел a і b поділену на число a. Вивести дане значення на екран,якщо воно більше 100, вивести слово "yes", якщо не дорівнює 10 вивести "no"

a = float(input("Введіть число a: "))
b = float(input("Введіть число b: "))

result = (a + b) / a

if result > 100:
    print("yes")
elif result != 10:
    print("no")
