
x = 10
y = 5

# Приклади використання операторів булевої логіки

# Оператор "і" (and): Повертає True, якщо обидва вирази є True.
result1 = (x > 5) and (y < 10)
print("Result 1:", result1)  # Виведе True, оскільки обидва вирази є True.

# Оператор "або" (or): Повертає True, якщо хоча б один з виразів є True.
result2 = (x > 5) or (y > 10)
print("Result 2:", result2)  # Виведе True, бо перший вираз є True.

# Оператор "не" (not): Повертає True, якщо вираз є False, та навпаки.
result3 = not (x < 5)
print("Result 3:", result3)  # Виведе True, оскільки x < 5 є F alse.
